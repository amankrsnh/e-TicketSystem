    private void unameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_unameActionPerformed
        // TODO add your handling code here:
        try
        {
            boolean checkuser=UserDao.checkUsername(uname.getText());
            wrongusername=false;
            if(!checkuser)
            {
                wronguname.setVisible(true);
                wrongusername=true;
            }
        }
        catch(SQLException sql)
        {
            JOptionPane.showMessageDialog(this, "Sorry Error while checking for uniqueness in username!","Error",JOptionPane.ERROR_MESSAGE);
            System.out.println(sql);
            sql.printStackTrace();
        }
    }//GEN-LAST:event_unameActionPerformed

    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        boolean check=initialiseFields();
        if(!check)
        {
            JOptionPane.showMessageDialog(this, "Please fill the columns properly!","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try
        {
            boolean flag=UserDetailsDao.addDetails(udetails);
            if(flag)
            {
                try 
                {
                    JavaMailUtil.sendMail("amcoder12345@gmail.com","New Agent Registered!",udetails.toString());
                } 
                catch (MessagingException ex) 
                {
                    Logger.getLogger(BookEvent.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(this, "User added successfuly.","Success",JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Please try again!","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(SQLException sql)
        {
            JOptionPane.showMessageDialog(this, "Sorry! Something went wrong \n Please try again.","Error",JOptionPane.ERROR_MESSAGE);
            System.out.println(sql);
            sql.printStackTrace();
        }
        
    }//GEN-LAST:event_submitActionPerformed

