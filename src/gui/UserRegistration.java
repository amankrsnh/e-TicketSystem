    private boolean initialiseFields() 
    {
        udetails=new UserDetails();
        udetails.setUsername(uname.getText());
        udetails.setName(name.getText());
        udetails.setPassword(pass.getText());
        udetails.setEmailId(emailid.getText());
        udetails.setDateOfBirth(dob.getText());
        udetails.setGender(gender.getText());
        udetails.setMobileNum(Long.parseLong(mobno.getText()));
        udetails.setAddress(address.getText());
        udetails.setCompanyName(compname.getText());
        if(udetails.getCompanyName().isEmpty())
            udetails.setCompanyName(null);
        udetails.setUserType(usertype);
        if(agentreg.isSelected())
        {
            if(udetails.getCompanyName()==null)
                return false;
        }
        if(wrongusername)
        {
            JOptionPane.showMessageDialog(this, "Username already exists!\nTry some new Username!","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return !udetails.getUserType().isEmpty() || !udetails.getUsername().isEmpty() || !udetails.getName().isEmpty() || !udetails.getPassword().isEmpty() || !udetails.getEmailId().isEmpty() || !udetails.getDateOfBirth().isEmpty() || !udetails.getGender().isEmpty() || !(udetails.getMobileNum()==0) || !udetails.getAddress().isEmpty();
    }
